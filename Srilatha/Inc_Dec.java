import java.util.*;

class Inc_Dec{
	public static void main(String args[]){
	Scanner d =new Scanner(System.in);
	System.out.println("Enter a number ");
	int a=d.nextInt();

	System.out.println(a++);
	System.out.println(++a);
	System.out.println(a);
	System.out.println(a--);
	System.out.println(--a);
	System.out.println(a);
	}
}